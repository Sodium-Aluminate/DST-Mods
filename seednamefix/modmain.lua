	GLOBAL.STRINGS.NAMES.KNOWN_ASPARAGUS_SEEDS = GLOBAL.STRINGS.NAMES.ASPARAGUS_SEEDS
	GLOBAL.STRINGS.NAMES.CARROT_SEEDS = GLOBAL.STRINGS.NAMES.CARROT_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.CARROT .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_CARROT_SEEDS = GLOBAL.STRINGS.NAMES.CARROT_SEEDS
	GLOBAL.STRINGS.NAMES.CORN_SEEDS = GLOBAL.STRINGS.NAMES.CORN_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.CORN .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_CORN_SEEDS = GLOBAL.STRINGS.NAMES.CORN_SEEDS
	GLOBAL.STRINGS.NAMES.DRAGONFRUIT_SEEDS = GLOBAL.STRINGS.NAMES.DRAGONFRUIT_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.DRAGONFRUIT .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_DRAGONFRUIT_SEEDS = GLOBAL.STRINGS.NAMES.DRAGONFRUIT_SEEDS
	GLOBAL.STRINGS.NAMES.DURIAN_SEEDS = GLOBAL.STRINGS.NAMES.DURIAN_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.DURIAN .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_DURIAN_SEEDS = GLOBAL.STRINGS.NAMES.DURIAN_SEEDS
	GLOBAL.STRINGS.NAMES.EGGPLANT_SEEDS = GLOBAL.STRINGS.NAMES.EGGPLANT_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.EGGPLANT .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_EGGPLANT_SEEDS = GLOBAL.STRINGS.NAMES.EGGPLANT_SEEDS
	GLOBAL.STRINGS.NAMES.GARLIC_SEEDS = GLOBAL.STRINGS.NAMES.GARLIC_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.GARLIC .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_GARLIC_SEEDS = GLOBAL.STRINGS.NAMES.GARLIC_SEEDS
