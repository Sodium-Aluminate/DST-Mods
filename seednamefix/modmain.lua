	GLOBAL.STRINGS.NAMES.KNOWN_PEPPER_SEEDS = GLOBAL.STRINGS.NAMES.PEPPER_SEEDS
	GLOBAL.STRINGS.NAMES.POMEGRANATE_SEEDS = GLOBAL.STRINGS.NAMES.POMEGRANATE_SEEDS .. "(" .. GLOBAL.STRINGS.NAMES.POMEGRANATE .. ")"
	GLOBAL.STRINGS.NAMES.KNOWN_POMEGRANATE_SEEDS = GLOBAL.STRINGS.NAMES.POMEGRANATE_SEEDS
